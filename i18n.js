(function () {
  const DICT = window.I18N_DICT;
  const STORE_KEY = 'cylgem_lang';

  function getInitialLang() {
    const saved = localStorage.getItem(STORE_KEY);
    if (saved === 'es' || saved === 'en') return saved;
    return (navigator.language || 'es').toLowerCase().startsWith('en') ? 'en' : 'es';
  }

  function applyStatic(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const entry = DICT[el.getAttribute('data-i18n')];
      if (entry && entry[lang] != null) el.innerHTML = entry[lang];
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const entry = DICT[el.getAttribute('data-i18n-ph')];
      if (entry && entry[lang] != null) el.setAttribute('placeholder', entry[lang]);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
      const entry = DICT[el.getAttribute('data-i18n-aria')];
      if (entry && entry[lang] != null) el.setAttribute('aria-label', entry[lang]);
    });
  }

  function updateToggleUI(lang) {
    document.querySelectorAll('.lang-opt').forEach(opt => {
      opt.classList.toggle('is-active', opt.dataset.lang === lang);
    });
  }

  window.setLang = function (lang) {
    if (lang !== 'es' && lang !== 'en') lang = 'es';
    document.documentElement.lang = lang;
    localStorage.setItem(STORE_KEY, lang);
    window.CYLGEM_LANG = lang;
    applyStatic(lang);
    updateToggleUI(lang);
    // Contenido dinámico de la base (hero/empresa/contacto) — definido en index.html
    if (typeof window.applyDbContent === 'function') window.applyDbContent(lang);
  };

  window.getLang = function () { return window.CYLGEM_LANG || getInitialLang(); };

  function bindToggle(el) {
    if (!el) return;
    el.addEventListener('click', e => {
      const opt = e.target.closest('.lang-opt');
      const target = (opt && opt.dataset.lang)
        ? opt.dataset.lang
        : (window.getLang() === 'es' ? 'en' : 'es');
      window.setLang(target);
    });
  }

  function init() {
    window.setLang(getInitialLang());
    bindToggle(document.getElementById('langToggle'));
    bindToggle(document.getElementById('mobileLangToggle'));
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
